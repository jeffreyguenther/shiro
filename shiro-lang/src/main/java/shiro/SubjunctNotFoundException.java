package shiro;

/**
 *
 * @author jeffreyguenther
 */
public class SubjunctNotFoundException extends Exception {

    /**
     * Creates a new instance of
     * <code>SubjunctNotFoundException</code> without detail message.
     */
    public SubjunctNotFoundException() {
    }

    /**
     * Constructs an instance of
     * <code>SubjunctNotFoundException</code> with the specified detail message.
     *
     * @param msg the detail message.
     */
    public SubjunctNotFoundException(String msg) {
        super(msg);
    }
}
